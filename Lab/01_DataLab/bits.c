/*
 * copyLSB - set all bits of result to least significant bit of x
 *   Example: copyLSB(5) = 0xFFFFFFFF, copyLSB(6) = 0x00000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int copyLSB(int x) {
    int LSB = x & 0x1; // Get the last bit by masking all other bits
    LSB = (LSB << 31) >> 31; // Shift it left and make it the MSB. Then arithmetic right shift to repeat it
    return LSB;
}
/*
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
    /* First check if number is positive or negative by extracting MSB
     * Then create a mask for 2^n - 1
     * Use this mask to calculate the remainder when divided by 2^n
     * If x < 0 and rem > 0, shift the remainder left to prevent losing it during the right shift
     * Finally shifting right will yield the required answer
     */
    int MSB = (x >> 31) & 1; // Positive (0) or negative (-1)
    int mask = (1 << n) + ~0; // 2^n - 1
    int rem = x & mask; // Remainder when divided by 2^n
    x = x + ((MSB & !!rem) << n);
    return (x >> n);
}
/*
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
    /* Shift right by n * 8 bits (n * 2^3 bits) to put the required byte at least significant position
     * Mask other bits and extract first byte
     */
    int result = x >> (n << 3);
    result = result & 0xff;
    return result;
}
/*
 * anyOddBit - return 1 if any odd-numbered bit in word set to 1
 *   Examples anyOddBit(0x5) = 0, anyOddBit(0x7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int anyOddBit(int x) {
    /* Get the required mask for 1 in odd-numbered bit positions
     * Mask the number and return the boolean value of that
     */
    int mask = (0xaa << 8) | 0xaa;
    mask = (mask << 16) | mask;
    x = (x & mask);
    return !!x;
}
/*
 * isNegative - return 1 if x < 0, return 0 otherwise
 *   Example: isNegative(-1) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNegative(int x) {
    /* Right arithmetic shift will either result in -1 (negative) or 0 (positive)
     * Convert this integer values into logical values by using two logical NOT's
     */
    int MSB = x >> 31;
    int result = !!MSB;
    return result;
}
/*
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
    /* Extract hex1 (least significant 4 bits)
     * If hex1 > 9, hex1 + 6 will overflow into bit 5. Catch this.
     * Right shift to lose hex1. The new number should be equal to 3.
     */
    int hex1 = 0xf & x;
    int byte1 = !(0xf0 & (hex1 + 6)); // a + 6 overflows into bit 5 when a >= 10
    int hex2 = x >> 4; // Right shifting to lost hex1
    int byte2 = !(hex2 + (~0x3) + 1); // 1 if hex2 = 0101
    return (byte1 & byte2);
}
/*
 * fitsBits - return 1 if x can be represented as an
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int fitsBits(int x, int n) {
    /* Create a mask with the sign of x.
     * Use this to extract the highest bit in x
     * Shift it right by (n - 1) to yield either 0 or a number.
     */
    int mask = x >> 31;
    int MSB = (~x & mask) + (x & ~mask);
    int result = !(MSB >> (n + ~0));
    return result;
}
/*
 * subOK - Determine if can compute x-y without overflow
 *   Example: subOK(0x80000000,0x80000000) = 1,
 *            subOK(0x80000000,0x70000000) = 0,
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int subOK(int x, int y) {
    /* Get x - y. Get signs of x, -y, and x - y
     * If x > 0 and -y > 0 and (x-y) <= 0, overflow
     * If x < 0 and -y < 0 and (x-y) >= 0, overflows
     */
    int sub = x + ~y + 1;
    int signX = !(x >> 31); // 1 if positive of 0
    int signMinusY = !!(y >> 31); //1 if positive or 0
    int signSub = !(sub >> 31); // 1 if positive or 0, 0 if negative
    int bothPos = (signX & signMinusY); // 1 if both positive
    int bothNeg = (!signX & !signMinusY); // 1 if both negative
    int result = (bothPos & !signSub) | (bothNeg & signSub);
    return !result;
}
/*
 * conditional - same as x ? y : z
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
    /* Convert x into a logical value
     * Shift left and arithmetic shift right to repeat the logical value
     * Mask y and z with the new proposition and its complement respectively
     * One will generate the value and the other will generate 0
     * Add them to generate solution
     */
    int proposition = ((!!x) << 31) >> 31; //  0xffffffff if true, 0x00000000 if false
    int result = (proposition & y) + (~proposition & z);
    return result;
}
